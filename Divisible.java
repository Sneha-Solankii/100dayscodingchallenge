class Divisible
 {
   public static void main(String[]args)
    {
      int num = 25;
      
      if(num%3==0 && num%7==0)
       
           System.out.println(num+"Number is divisible");
      
      else 
             
          System.out.println(num+"Number is not divisible");
      
     
     }
 }    
